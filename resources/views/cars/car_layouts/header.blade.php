    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const sidebar = document.getElementById('sidebar');
            const sidebarToggle = document.getElementById('sidebarToggle');
            const content = document.getElementById('content');

            // Add the 'closed' class to the sidebar by default
            sidebar.classList.add('closed');

            sidebarToggle.addEventListener('click', () => {
                sidebar.classList.toggle('closed');
                content.classList.toggle('content-closed', !sidebar.classList.contains('closed'));

                // Select the toggle button icon element
                const toggleButtonIcon = sidebarToggle.querySelector('i.material-icons');

                // Add or remove styles from the toggle button icon
                if (sidebar.classList.contains('closed')) {
                    // Sidebar is closed, add styles to the icon
                    toggleButtonIcon.style.color = ''; // Example style


                } else {



                    toggleButtonIcon.style.color = 'red';





                }
            });
        });
    </script>







